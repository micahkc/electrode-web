import { existsSync, mkdtempSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';
import { createHash } from 'node:crypto';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const schemaDir = resolve(root, 'schemas/flatbuffers');
// The Synapse log-container schema (`synapse.log.LogRecord`) is vendored under
// `schemas/flatbuffers/synapse_log.fbs`: the published `@cognipilot/synapse-fbs`
// package ships only the live topic schemas, not this logging format. The file
// is self-contained and byte-identical to upstream `fbs/log.fbs`.
const synapseSchemaDir = schemaDir;
if (!existsSync(join(synapseSchemaDir, 'synapse_log.fbs'))) {
  throw new Error(
    `Missing ${join(synapseSchemaDir, 'synapse_log.fbs')} — the vendored Synapse log schema.`
  );
}
const outFile = resolve(root, 'packages/electrode-flatbuffers/src/schema-assets.ts');

const assets = [
  {
    exportName: 'SYNAPSE_LOG_SCHEMA_ASSET',
    name: 'synapse.log',
    rootType: 'synapse.log.LogRecord',
    fileId: 'SYLG',
    includeDirs: [synapseSchemaDir],
    files: [resolve(synapseSchemaDir, 'synapse_log.fbs')],
    bfbsSource: resolve(synapseSchemaDir, 'synapse_log.fbs')
  },
  {
    exportName: 'ELECTRODE_GCS_LOG_SCHEMA_ASSET',
    name: 'electrode.gcs',
    rootType: 'electrode.gcs.GcsFrame',
    fileId: 'EGCS',
    includeDirs: [schemaDir],
    files: [
      resolve(schemaDir, 'common.fbs'),
      resolve(schemaDir, 'state.fbs'),
      resolve(schemaDir, 'events.fbs'),
      resolve(schemaDir, 'gcs_log.fbs')
    ],
    bfbsSource: resolve(schemaDir, 'gcs_log.fbs')
  }
];

const tempDir = mkdtempSync(resolve(tmpdir(), 'electrode-log-schema-'));

try {
  const renderedAssets = assets.map((asset) => renderAsset(asset));
  const source = `// Generated by scripts/generate-log-schema-assets.mjs. Do not edit by hand.

export interface FlatbufferSchemaAsset {
  name: string;
  rootType: string;
  fileId: string;
  fbsText: string;
  fbsSha256: string;
  bfbsBase64: string;
}

${renderedAssets.join('\n\n')}

export function schemaIdFromSha256(sha256: string): bigint {
  return BigInt(\`0x\${sha256.slice(0, 16)}\`);
}

export function decodeBase64Bytes(value: string): Uint8Array {
  const binary = atob(value);
  const bytes = new Uint8Array(binary.length);
  for (let index = 0; index < binary.length; index += 1) {
    bytes[index] = binary.charCodeAt(index);
  }
  return bytes;
}
`;

  writeFileSync(outFile, source);
} finally {
  rmSync(tempDir, { recursive: true, force: true });
}

function renderAsset(asset) {
  const fbsText = asset.files.map((file) => readFileSync(file, 'utf8').trim()).join('\n\n');
  const fbsSha256 = createHash('sha256').update(fbsText).digest('hex');
  const bfbsPath = generateBfbs(asset);
  const bfbsBase64 = readFileSync(bfbsPath).toString('base64');

  return `export const ${asset.exportName}: FlatbufferSchemaAsset = {
  name: ${JSON.stringify(asset.name)},
  rootType: ${JSON.stringify(asset.rootType)},
  fileId: ${JSON.stringify(asset.fileId)},
  fbsText: ${JSON.stringify(fbsText)},
  fbsSha256: ${JSON.stringify(fbsSha256)},
  bfbsBase64: ${JSON.stringify(bfbsBase64)}
};`;
}

function generateBfbs(asset) {
  const outputDir = resolve(tempDir, asset.name.replaceAll('.', '-'));
  const args = ['--binary', '--schema', '-o', outputDir];

  for (const includeDir of asset.includeDirs) {
    args.push('-I', includeDir);
  }

  args.push(asset.bfbsSource);
  const flatc = spawnSync('flatc', args, { stdio: 'inherit' });

  if (flatc.error?.code === 'ENOENT') {
    throw new Error('flatc was not found. Install FlatBuffers to generate schema assets.');
  }

  if (flatc.status !== 0) {
    throw new Error(`flatc failed while generating ${asset.name}`);
  }

  const stem = asset.bfbsSource.split('/').at(-1).replace(/\.fbs$/, '.bfbs');
  return resolve(outputDir, stem);
}
